    public void adicionaMp(ArrayList<BemInformada> pessoab){
        int atualx, atualy;
        Date current;
        
        for(int i =0; i <pessoab.size(); i++){
            for(int j = i+1; j <pessoab.size(); j++){
                if(pessoab.get(i).getX() == pessoab.get(j).getX() && pessoab.get(i).getY() == pessoab.get(j).getY()){
                    pessoab.get(i).addAgenda(j);
                    pessoab.get(j).addAgenda(i);
                }
                else if(pessoab.get(i).getX() == (pessoab.get(j).getX()-1) && pessoab.get(i).getY() == pessoab.get(j).getY()){
                    pessoab.get(i).addAgenda(j);
                    pessoab.get(j).addAgenda(i);
                    
                }
                else if(pessoab.get(i).getX() == (pessoab.get(j).getX()+1) && pessoab.get(i).getY() == pessoab.get(j).getY()){
                    pessoab.get(i).addAgenda(j);
                    pessoab.get(j).addAgenda(i);
                }
                else if(pessoab.get(i).getX() == pessoab.get(j).getX() && pessoab.get(i).getY() == (pessoab.get(j).getY()-1)){
                    pessoab.get(i).addAgenda(j);
                    pessoab.get(j).addAgenda(i);
                }
                else if(pessoab.get(i).getX() == pessoab.get(j).getX() && pessoab.get(i).getY() == (pessoab.get(j).getY()+1)){
                    pessoab.get(i).addAgenda(j);
                    pessoab.get(j).addAgenda(i);
                }
                
            }
            
        }
        
        for(int i = 0; i<pessoab.size(); i++){
            atualx = pessoab.get(i).getX();
            atualy = pessoab.get(i).getY();
            ArrayList <Integer> lista = pessoab.get(i).getAgenda();
            
            if((atualx >=4 && atualx<=9 )&& (atualy >=3 && atualy<=11)){
                pessoab.get(i).setCor(6); 
                
            }
            if((atualx >= 7 && atualx<=10 )&& (atualy >=45 && atualy <=53)){
                pessoab.get(i).setCor(5);
            }
            
            current = new Date();
            
            mapa[atualx][atualy] = pessoab.get(i).getCor();
            for (int j = 0; j < lista.size(); j++) {
                if(pessoab.get(i).getCor() == 6){
                    pessoab.get(lista.get(j)).setCor(6);
                    mapa[pessoab.get(lista.get(j)).getX()][pessoab.get(lista.get(j)).getY()] = pessoab.get(i).getCor();
                }else{
                    pessoab.get(lista.get(j)).setCor(5);
                    mapa[pessoab.get(lista.get(j)).getX()][pessoab.get(lista.get(j)).getY()] = pessoab.get(i).getCor();
                }
            }
        }
        
    }
    
